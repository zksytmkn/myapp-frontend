<template>
  <v-text-field
    v-model="setPassword"
    label="パスワードを入力"
    placeholder="8文字以上"
    outlined
  />
</template>

<script>
export default {
  props: {
    password: {
      type: String,
      default: ''
    }
  },
  computed: {
    setPassword: {
      get () {
        return this.password
      },
      set (newValue) {
        return this.$emit('update:password',newValue)
      }
    }
  }
}
</script>